# Sample dataset
data <- c(5, 8, 6, 9, 7, 10, 8, 6, 9, 10)

# Mean, Median, Variance, and Standard Deviation
mean(data)            # Mean
median(data)          # Median
var(data)             # Variance
sd(data)              # Standard Deviation

# Five-number summary
summary(data)

sort(data)

